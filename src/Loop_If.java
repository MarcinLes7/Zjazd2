public class Loop_If {
    public static void main(String[] args) {
        int x = 5;
        int y = 5;
        if (x > y) {
            System.out.println( x + " jest większe od " + y);
        } else if (x < y) {
            System.out.println(x + " jest mniejsze od " + y);
        } else {
            System.out.println(x + " jest równe " + y);
        }
    }
}
