public class LoopIfGrade {
    public static void main(String[] args) {
        char grade;
        grade = 'z';
        if (grade == 'A') {
            System.out.println("Świetnie");
        } else if (grade == 'B') {
            System.out.println("Dobrze");
        } else if (grade == 'D') {
            System.out.println("Mogło być lepiej");
        } else {
            System.out.println("Rozwaz oszukiwanie");
        }

    }
}
